#!/usr/bin/env python3

number_1 = int(input("Give me the first number: "))
number_2 = int(input("Give me the second number: "))

print("Thank you!")

print(f"{number_1} + {number_2} = {number_1 + number_2}")
print(f"{number_1} - {number_2} = {number_1 - number_2}")
print(f"{number_1} / {number_2} = {number_1 // number_2}")
print(f"{number_1} * {number_2} = {number_1 * number_2}")