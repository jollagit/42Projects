#include "libft.h"

int	ft_print_str(char *s)
{
	size_t	t;

	t = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (s[t])
	{
		write (1, &s[t], 1);
		t++;
	}
	return (t);
}
