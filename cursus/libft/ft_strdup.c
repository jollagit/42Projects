#include "libft.h"

char	*ft_strdup(const char *s)
{
	char			*ptr;
	unsigned int	i;

	i = 0;
	while (s[i])
		i++;
	ptr = (char *) malloc ((i + 1) * sizeof(char));
	i = 0;
	while (s[i])
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}

